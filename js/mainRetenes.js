  function GuardarRetenesToyotaCarrito(Retenes_toyota) {
    localStorage.setItem("carrito", JSON.stringify(Retenes_toyota));
  }
  function CargarRetenesToyotaCarrito() {
    return JSON.parse(localStorage.getItem("carrito")) || [];
  }
  
  function BuscarObjetoRetenesToyota(id) {
    const Retenes_toyota = CargarRetenesToyotaLS();
    return Retenes_toyota.find((elemento) => elemento.id == id);
  }
  function AgregarAlCarritoRetenesToyota(id) {
    const productos_carrito = CargarRetenesToyotaCarrito();
    let posicion = productos_carrito.findIndex(item =>item.id === id)
    if(posicion > -1) {
       productos_carrito[posicion].cantidad += 1;
    }else{
       const objeto = BuscarObjetoRetenesToyota(id);
       objeto.cantidad = 1;
       productos_carrito.push(objeto);
    } 
    Swal.fire({
      icon: 'success',
      title: 'Felicitaciones!!',
      text: 'Producto agregado correctamente al carrito ',
    })
    GuardarRetenesToyotaCarrito(productos_carrito);
    ActualizarRetenesToyota();
  }
  
