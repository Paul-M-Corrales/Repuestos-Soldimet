  function GuardarGuiasToyotaCarrito(Guias_toyota) {
    localStorage.setItem("carrito", JSON.stringify(Guias_toyota));
  }
  function CargarGuiasToyotaCarrito() {
    return JSON.parse(localStorage.getItem("carrito")) || [];
  }
  
  function BuscarObjetoGuiasToyota(id) {
    const Guias_toyota = CargarGuiasToyotaLS();
    return Guias_toyota.find((elemento) => elemento.id == id);
  }
  function AgregarAlCarritoGuiasToyota(id) {
    const productos_carrito = CargarGuiasToyotaCarrito();
    let posicion = productos_carrito.findIndex(item =>item.id === id)
    if(posicion > -1) {
       productos_carrito[posicion].cantidad += 1;
    }else{
       const objeto = BuscarObjetoGuiasToyota(id);
       objeto.cantidad = 1;
       productos_carrito.push(objeto);
    } 
    Swal.fire({
      icon: 'success',
      title: 'Felicitaciones!!',
      text: 'Producto agregado correctamente al carrito ',
    })
    GuardarGuiasToyotaCarrito(productos_carrito);
    ActualizarGuiasToyota();
  }
  
