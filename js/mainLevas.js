  function BuscarObjetoLevasChevrolet(id) {
    const Levas_chevrolet = CargarLevasChevroletLS();
    return Levas_chevrolet.find((elemento) => elemento.id == id);
  }
  function BuscarObjetoLevasChevrolet(id) {
    const Levas_chevrolet = CargarLevasChevroletLS();
    return Levas_chevrolet.find((elemento) => elemento.id == id);
  }

  function AgregarAlCarritoLevasChevrolet(id) {
    const productos_carrito = CargarLevasChevroletCarrito();
    let posicion = productos_carrito.findIndex(item =>item.id === id)
     if(posicion > -1) {
        productos_carrito[posicion].cantidad += 1;
     }else{
        const objeto = BuscarObjetoLevasChevrolet(id);
        objeto.cantidad = 1;
        productos_carrito.push(objeto);
     }   
     Swal.fire({
      icon: 'success',
      title: 'Felicitaciones!!',
      text: 'Producto agregado correctamente al carrito ',
    })
    
    GuardarLevasChevroletCarrito(productos_carrito);
    ActualizarLevasChevrolet();
   
  }

