  function GuardarLevasToyotaCarrito(Levas_toyota) {
    localStorage.setItem("carrito", JSON.stringify(Levas_toyota));
  }
  function CargarLevasToyotaCarrito() {
    return JSON.parse(localStorage.getItem("carrito")) || [];
  }
  
  function BuscarObjetoLevasToyota(id) {
    const Levas_toyota = CargarLevasToyotaLS();
    return Levas_toyota.find((elemento) => elemento.id == id);
  }
  function AgregarAlCarritoLevasToyota(id) {
    const productos_carrito = CargarLevasToyotaCarrito();
    let posicion = productos_carrito.findIndex(item =>item.id === id)
    if(posicion > -1) {
       productos_carrito[posicion].cantidad += 1;
    }else{
       const objeto = BuscarObjetoLevasToyota(id);
       objeto.cantidad = 1;
       productos_carrito.push(objeto);
    } 
    Swal.fire({
      icon: 'success',
      title: 'Felicitaciones!!',
      text: 'Producto agregado correctamente al carrito ',
    })
    GuardarLevasToyotaCarrito(productos_carrito);
    ActualizarLevasToyota();
  }
  
